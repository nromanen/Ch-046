public class ClassroomErrors {

    private String nameError;
    private String capacityError;
    private String typesError;

    public String getNameError() {
        return nameError;
    }

    public void setNameError(String nameError) {
        this.nameError = nameError;
    }

    public String getCapacityError() {
        return capacityError;
    }

    public void setCapacityError(String capacityError) {
        this.capacityError = capacityError;
    }

    public String getTypesError() {
        return typesError;
    }

    public void setTypesError(String typesError) {
        this.typesError = typesError;
    }
}
